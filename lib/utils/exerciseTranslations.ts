export function translateExerciseName(name: string): string {
  // Check exact match first
  if (translations[name]) return translations[name];

  // Check case-insensitive exact match
  const lower = name.toLowerCase();
  for (const [en, pt] of Object.entries(translations)) {
    if (en.toLowerCase() === lower) return pt;
  }

  // Apply word-by-word replacements
  let translated = name;
  for (const [regex, replacement] of wordReplacements) {
    translated = translated.replace(regex, replacement);
  }

  // If translation changed the string, return it
  if (translated !== name) return translated;

  // Return original
  return name;
}
